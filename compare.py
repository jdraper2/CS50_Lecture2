# Ask the user for the value of x and convert it to an integer
x = int(input("What's x: "))
# Ask the user for the value of y and convert it to an integer
y = int(input("What's y: "))

# Compare the values of x and y
if x < y:
    # If x is less than y, print this message
    print("x is less than y")
elif x > y:
    # If x is greater than y, print this message
    print("x is greater than y")
else:
    # If x is equal to y, print this message
    print("x is equal to y")